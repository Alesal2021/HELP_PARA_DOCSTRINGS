def soma(x = 0, y = 0, z = 0):
    """
    Clica 3 asterisco e depois ENTER
    Função que soma ate 3 valores
    :param x:Valor inteiros opcionais
    :param y:Valor inteiros opcionais
    :param z:Valor inteiros opcionais
    :return:A soma dos inteiros x,y,z
    """
    return x + y + z

